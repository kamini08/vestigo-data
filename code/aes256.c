typedef struct {
    uint8_t round_keys[AES_ROUNDS + 1][16];
} AES256_CTX;

static uint8_t xtime(uint8_t x) {
    return ((x << 1) ^ (((x >> 7) & 1) * 0x1b));
}

static void sub_bytes(uint8_t *state) {
    for (int i = 0; i < 16; i++) {
        state[i] = sbox[state[i]];
    }
}

static void shift_rows(uint8_t *state) {
    uint8_t temp;
    
    temp = state[1];
    state[1] = state[5];
    state[5] = state[9];
    state[9] = state[13];
    state[13] = temp;
    
    temp = state[2];
    state[2] = state[10];
    state[10] = temp;
    temp = state[6];
    state[6] = state[14];
    state[14] = temp;
    
    temp = state[15];
    state[15] = state[11];
    state[11] = state[7];
    state[7] = state[3];
    state[3] = temp;
}

static void mix_columns(uint8_t *state) {
    uint8_t tmp[16];
    
    for (int i = 0; i < 4; i++) {
        int idx = i * 4;
        tmp[idx + 0] = (uint8_t)(xtime(state[idx + 0]) ^ xtime(state[idx + 1]) ^ state[idx + 1] ^ state[idx + 2] ^ state[idx + 3]);
        tmp[idx + 1] = (uint8_t)(state[idx + 0] ^ xtime(state[idx + 1]) ^ xtime(state[idx + 2]) ^ state[idx + 2] ^ state[idx + 3]);
        tmp[idx + 2] = (uint8_t)(state[idx + 0] ^ state[idx + 1] ^ xtime(state[idx + 2]) ^ xtime(state[idx + 3]) ^ state[idx + 3]);
        tmp[idx + 3] = (uint8_t)(xtime(state[idx + 0]) ^ state[idx + 0] ^ state[idx + 1] ^ state[idx + 2] ^ xtime(state[idx + 3]));
    }
    
    memcpy(state, tmp, 16);
}

static void add_round_key(uint8_t *state, const uint8_t *round_key) {
    for (int i = 0; i < 16; i++) {
        state[i] ^= round_key[i];
    }
}

static void key_expansion(const uint8_t *key, AES256_CTX *ctx) {
    uint8_t temp[4];
    uint32_t *w = (uint32_t *)ctx->round_keys;
    int i;
    
    // Copy initial key (8 words = 32 bytes)
    memcpy(ctx->round_keys, key, 32);
    
    // Generate remaining key words
    for (i = 8; i < 4 * (AES_ROUNDS + 1); i++) {
        memcpy(temp, &w[i - 1], 4);
        
        if (i % 8 == 0) {
            // RotWord
            uint8_t t = temp[0];
            temp[0] = temp[1];
            temp[1] = temp[2];
            temp[2] = temp[3];
            temp[3] = t;
            
            // SubWord
            temp[0] = sbox[temp[0]];
            temp[1] = sbox[temp[1]];
            temp[2] = sbox[temp[2]];
            temp[3] = sbox[temp[3]];
            
            temp[0] ^= rcon[i / 8];
        } else if (i % 8 == 4) {
            // SubWord (for AES-256 only)
            temp[0] = sbox[temp[0]];
            temp[1] = sbox[temp[1]];
            temp[2] = sbox[temp[2]];
            temp[3] = sbox[temp[3]];
        }
        
        uint32_t prev = w[i - 8];
        uint32_t new_word;
        memcpy(&new_word, temp, 4);
        w[i] = prev ^ new_word;
    }
}

void aes256_encrypt(const uint8_t *plaintext, uint8_t *ciphertext, const uint8_t *key) {
    AES256_CTX ctx;
    uint8_t state[16];
    
    key_expansion(key, &ctx);
    memcpy(state, plaintext, 16);
    
    add_round_key(state, ctx.round_keys[0]);
    
    for (int round = 1; round < AES_ROUNDS; round++) {
        sub_bytes(state);
        shift_rows(state);
        mix_columns(state);
        add_round_key(state, ctx.round_keys[round]);
    }
    
    sub_bytes(state);
    shift_rows(state);
    add_round_key(state, ctx.round_keys[AES_ROUNDS]);
    
    memcpy(ciphertext, state, 16);
}

