# Minimum match lengths for reliable detection
MIN_MATCH_LENGTHS = {
    'AES_SBOX': 64,        # Match at least 64 consecutive bytes
    'DES_SBOX': 32,        # DES S-boxes are smaller
    'SHA256_K': 16,        # Match at least 4 constants (16 bytes)
    'MD5_K': 16,           # Match at least 4 constants
    'Generic': 16,         # Default minimum
}

def get_constant_signature(name):
    """Get byte signature for a constant (for pattern matching)."""
    if name == 'AES_SBOX':
        return AES_SBOX[:64]  # First 64 bytes are highly distinctive
    elif name == 'AES_INV_SBOX':
        return AES_INV_SBOX[:64]
    elif name == 'SHA256_K':
        # Convert first 4 constants to bytes (big-endian)
        return b''.join(k.to_bytes(4, 'big') for k in SHA256_K[:4])
    elif name == 'SHA256_H':
        return b''.join(h.to_bytes(4, 'big') for h in SHA256_H)
    elif name == 'SHA1_H':
        return b''.join(h.to_bytes(4, 'big') for h in SHA1_H)
    elif name == 'MD5_IV':
        return b''.join(iv.to_bytes(4, 'little') for iv in MD5_IV)  # MD5 uses little-endian
    else:
        return None

