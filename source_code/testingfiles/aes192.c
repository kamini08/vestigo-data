static const uint8_t RCON[13] = {
    0x00,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1B,0x36,0x6C,0xD8
};

static uint8_t xt(uint8_t x) {
    return (uint8_t)((x << 1) ^ ((x & 0x80) ? 0x1B : 0));
}

static void SubBytes(uint8_t *s) {
    for(int i=0;i<16;i++)
        s[i] = SBOX[s[i]];
}

static void ShiftRows(uint8_t *s) {
    uint8_t t[16];
    memcpy(t, s, 16);

    s[1]  = t[5];  s[5]  = t[9];  s[9]  = t[13]; s[13] = t[1];
    s[2]  = t[10]; s[10] = t[2];  s[6]  = t[14]; s[14] = t[6];
    s[3]  = t[15]; s[7]  = t[3];  s[11] = t[7];  s[15] = t[11];
}

static void MixColumns(uint8_t *s) {
    for(int i=0;i<4;i++){
        uint8_t a = s[4*i+0], b = s[4*i+1], c = s[4*i+2], d = s[4*i+3];
        s[4*i+0] = xt(a) ^ xt(b) ^ b ^ c ^ d;
        s[4*i+1] = a ^ xt(b) ^ xt(c) ^ c ^ d;
        s[4*i+2] = a ^ b ^ xt(c) ^ xt(d) ^ d;
        s[4*i+3] = xt(a) ^ a ^ b ^ c ^ xt(d);
    }
}

static void AddRoundKey(uint8_t *s, uint8_t rk[16]) {
    for(int i=0;i<16;i++) s[i] ^= rk[i];
}

static void KeyExpansion(uint8_t key[24], uint8_t roundKeys[13][16]) {
    uint8_t temp[4];
    uint8_t expanded[208];  // (Nr+1)*16 = 208 bytes

    memcpy(expanded, key, 24);

    int bytesUsed = 24;
    int rconIter = 1;

    while (bytesUsed < 208) {
        for(int i=0;i<4;i++)
            temp[i] = expanded[bytesUsed - 4 + i];

        if (bytesUsed % 24 == 0) {
            uint8_t t = temp[0];
            temp[0] = SBOX[temp[1]];
            temp[1] = SBOX[temp[2]];
            temp[2] = SBOX[temp[3]];
            temp[3] = SBOX[t];
            temp[0] ^= RCON[rconIter++];
        }

        for(int i=0;i<4;i++)
            expanded[bytesUsed++] = expanded[bytesUsed - 24] ^ temp[i];
    }

    for(int r=0;r<Nr+1;r++)
        memcpy(roundKeys[r], expanded + (r*16), 16);
}

void AES192_Encrypt(uint8_t *in, uint8_t *out, uint8_t key[24]) {
    uint8_t state[16];
    uint8_t rk[13][16];

    memcpy(state, in, 16);
    KeyExpansion(key, rk);

    AddRoundKey(state, rk[0]);

    for(int round=1; round<Nr; round++){
        SubBytes(state);
        ShiftRows(state);
        MixColumns(state);
        AddRoundKey(state, rk[round]);
    }

    SubBytes(state);
    ShiftRows(state);
    AddRoundKey(state, rk[Nr]);

    memcpy(out, state, 16);
}

