static const uint8_t RCON[15] = {
    0x00,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1B,0x36,0x6C,0xD8,0xAB,0x4D
};

static inline uint8_t xtime(uint8_t x) {
    return (uint8_t)((x << 1) ^ ((x & 0x80) ? 0x1B : 0));
}

static void SubBytes(uint8_t *s) {
    for (int i = 0; i < 16; ++i) s[i] = SBOX[s[i]];
}

static void ShiftRows(uint8_t *s) {
    uint8_t t[16];
    memcpy(t, s, 16);
    s[1]  = t[5];  s[5]  = t[9];  s[9]  = t[13]; s[13] = t[1];
    s[2]  = t[10]; s[10] = t[2];  s[6]  = t[14]; s[14] = t[6];
    s[3]  = t[15]; s[7]  = t[3];  s[11] = t[7];  s[15] = t[11];
}

static void MixColumns(uint8_t *s) {
    for (int i = 0; i < 4; ++i) {
        int a = 4 * i;
        uint8_t x0 = s[a], x1 = s[a+1], x2 = s[a+2], x3 = s[a+3];
        s[a]   = xtime(x0) ^ xtime(x1) ^ x1 ^ x2 ^ x3;
        s[a+1] = x0 ^ xtime(x1) ^ xtime(x2) ^ x2 ^ x3;
        s[a+2] = x0 ^ x1 ^ xtime(x2) ^ xtime(x3) ^ x3;
        s[a+3] = xtime(x0) ^ x0 ^ x1 ^ x2 ^ xtime(x3);
    }
}

static void AddRoundKey(uint8_t *s, const uint8_t *rk) {
    for (int i = 0; i < 16; ++i) s[i] ^= rk[i];
}

/* RotWord: rotate 4-byte word left by 1 byte */
static void RotWord(uint8_t *w) {
    uint8_t t = w[0];
    w[0] = w[1]; w[1] = w[2]; w[2] = w[3]; w[3] = t;
}

/* SubWord: apply SBOX to 4-byte word */
static void SubWord(uint8_t *w) {
    w[0] = SBOX[w[0]]; w[1] = SBOX[w[1]];
    w[2] = SBOX[w[2]]; w[3] = SBOX[w[3]];
}

/* KeyExpansion for AES-256: expand 32-byte key to 240 bytes (15 round keys * 16) */
static void KeyExpansion(const uint8_t key[32], uint8_t roundKeys[EXPANDED_KEY_BYTES]) {
    memcpy(roundKeys, key, 32);
    int bytes = 32;
    uint8_t temp[4];
    int rcon_idx = 1;

    while (bytes < EXPANDED_KEY_BYTES) {
        /* temp = previous 4 bytes */
        for (int i = 0; i < 4; ++i) temp[i] = roundKeys[bytes - 4 + i];

        if (bytes % (Nk * 4) == 0) {
            RotWord(temp);
            SubWord(temp);
            temp[0] ^= RCON[rcon_idx++];
        } else if (Nk > 6 && (bytes % (Nk * 4) == 16)) {
            /* extra SubWord step for AES-256 */
            SubWord(temp);
        }

        for (int i = 0; i < 4; ++i) {
            roundKeys[bytes] = roundKeys[bytes - (Nk * 4)] ^ temp[i];
            ++bytes;
        }
    }
}

void AES256_Encrypt(const uint8_t in[16], uint8_t out[16], const uint8_t key[32]) {
    uint8_t state[16];
    uint8_t roundKeys[EXPANDED_KEY_BYTES];

    memcpy(state, in, 16);
    KeyExpansion(key, roundKeys);

    AddRoundKey(state, roundKeys + 0);

    for (int round = 1; round < Nr; ++round) {
        SubBytes(state);
        ShiftRows(state);
        MixColumns(state);
        AddRoundKey(state, roundKeys + (16 * round));
    }

    SubBytes(state);
    ShiftRows(state);
    AddRoundKey(state, roundKeys + (16 * Nr));

    memcpy(out, state, 16);
}

