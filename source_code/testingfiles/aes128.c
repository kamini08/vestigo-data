static const uint8_t RCON[11] = {
    0x00,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1B,0x36
};

static uint8_t xt(uint8_t a) {
    return (uint8_t)((a << 1) ^ ((a & 0x80) ? 0x1B : 0));
}

static void SubBytes(uint8_t *state) {
    for(int i = 0; i < 16; i++)
        state[i] = SBOX[state[i]];
}

static void ShiftRows(uint8_t *s) {
    uint8_t t[16];
    memcpy(t, s, 16);

    s[1]  = t[5];  s[5]  = t[9];  s[9]  = t[13]; s[13] = t[1];
    s[2]  = t[10]; s[10] = t[2];  s[6]  = t[14]; s[14] = t[6];
    s[3]  = t[15]; s[7]  = t[3];  s[11] = t[7];  s[15] = t[11];
}

static void MixColumns(uint8_t *s) {
    for(int i=0;i<4;i++){
        int a = i*4;
        uint8_t x0=s[a], x1=s[a+1], x2=s[a+2], x3=s[a+3];

        s[a]   = xt(x0) ^ xt(x1) ^ x1 ^ x2 ^ x3;
        s[a+1] = x0 ^ xt(x1) ^ xt(x2) ^ x2 ^ x3;
        s[a+2] = x0 ^ x1 ^ xt(x2) ^ xt(x3) ^ x3;
        s[a+3] = xt(x0) ^ x0 ^ x1 ^ x2 ^ xt(x3);
    }
}

static void AddRoundKey(uint8_t *s, uint8_t rk[16]) {
    for(int i=0;i<16;i++) s[i] ^= rk[i];
}

static void KeyExpansion(uint8_t key[16], uint8_t roundKeys[11][16]) {
    memcpy(roundKeys[0], key, 16);

    for(int r=1;r<=10;r++){
        uint8_t *prev = roundKeys[r-1];
        uint8_t *curr = roundKeys[r];

        curr[0] = prev[0] ^ SBOX[prev[13]] ^ RCON[r];
        curr[1] = prev[1] ^ SBOX[prev[14]];
        curr[2] = prev[2] ^ SBOX[prev[15]];
        curr[3] = prev[3] ^ SBOX[prev[12]];

        for(int i=4;i<16;i++)
            curr[i] = prev[i] ^ curr[i-4];
    }
}

void AES128_Encrypt(uint8_t *in, uint8_t *out, uint8_t key[16]) {
    uint8_t state[16];
    uint8_t rk[11][16];

    memcpy(state, in, 16);
    KeyExpansion(key, rk);

    AddRoundKey(state, rk[0]);

    for(int round=1; round<Nr; round++){
        SubBytes(state);
        ShiftRows(state);
        MixColumns(state);
        AddRoundKey(state, rk[round]);
    }

    SubBytes(state);
    ShiftRows(state);
    AddRoundKey(state, rk[Nr]);

    memcpy(out, state, 16);
}

